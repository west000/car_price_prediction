DROP TABLE IF EXISTS `carModel`;
CREATE TABLE `carModel` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `brand` varchar(255) NOT NULL COMMENT '品牌',
  `uniqueId` varchar(255) DEFAULT NULL COMMENT '车辆ID，必须唯一，防止重复爬取',
  `carVersion` varchar(255) DEFAULT NULL COMMENT '车型号',
  `firstLicence` datetime DEFAULT NULL COMMENT '首次上牌日期',
  `mailage` int(11) DEFAULT NULL COMMENT '行驶里程数',
  `newPrice` int(11) DEFAULT NULL COMMENT '新车价格',
  `comeFrom` tinyint(4) DEFAULT NULL COMMENT '来源，商家0，个人1',
  `area` varchar(255) DEFAULT NULL COMMENT '地区',
  `color` varchar(255) DEFAULT NULL COMMENT '颜色',
  `transferTimes` int(11) DEFAULT NULL COMMENT '过户次数',
  `addDate` datetime DEFAULT NULL COMMENT '上架日期',
  `fixRecord` text COMMENT '维修记录',
  `url` varchar(255) DEFAULT NULL COMMENT '车辆链接',
  `currentPrice` int(11) DEFAULT NULL COMMENT '当前价格',
  `isCrawled` tinyint(4) NOT NULL DEFAULT '0' COMMENT '该骑车信息是否完全爬取',
  `updateDate` datetime DEFAULT NULL COMMENT '更新时间',
  `dateVersion` varchar(255) DEFAULT NULL COMMENT '哪年款',
  `site` varchar(255) DEFAULT NULL COMMENT '来源网站',
  `isImport` tinyint(1) DEFAULT '0' COMMENT '是否是进口车',
  `disRank` varchar(255) DEFAULT NULL COMMENT '排量规范',
  `publishTime` datetime DEFAULT NULL COMMENT '上架时间',
  `saleDate` datetime DEFAULT NULL COMMENT '最终售出时间',
  `carAge` int(2) DEFAULT NULL,
  `premiumRate` float(10,4) DEFAULT NULL,
  `deleted` int(1) DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `car2_unique_id` (`uniqueId`),
  KEY `brand` (`brand`),
  KEY `car_deleted` (`deleted`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;